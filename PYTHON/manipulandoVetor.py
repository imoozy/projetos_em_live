# Manipulando vetor

vetor = [1, 2, 3, 4, 5] 

print(f"Vetor original: {vetor}")
print(f"Posição 2: {vetor[2]}")  # Acessando o elemento na posição 2 (terceiro elemento

vetor[0] = -55  # Modificando o elemento na posição 0 (primeiro elemento)

print(f"Vetor modificado: {vetor}")

# caracteristicas do vetor
print(f"Tamanho do vetor: {len(vetor)}")  # Tamanho do vetor
print(f"Vetor invertido: {vetor[::-1]}")  # Invertendo o vetor
print(f"Vetor ordenado: {sorted(vetor)}")  # Ordenando o vetor
